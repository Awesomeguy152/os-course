#include "vtsh.h"  // общие объявления для библиотеки, включая прототип vtsh_prompt и константы приглашения

/* --------------------------------------------------------------------------
 * vtsh_prompt — точка централизованного определения приглашения оболочки
 * --------------------------------------------------------------------------
 *  Функция выделена в отдельный модуль, чтобы строка приглашения использовалась
 *  как из исполняемого файла, так и из тестов/утилит. Это позволяет менять текст
 *  приглашения в одном месте без риска рассинхронизации между бинарником и
 *  Python-тестами, которые ожидают конкретный префикс. Возвращаем значение как
 *  строковый литерал, что не требует освобождения памяти вызывающим кодом.
 */
const char* vtsh_prompt() {
  return "vtsh> ";  // стандартное приглашение vtsh, совпадающее с тестовыми ожиданиями
}
